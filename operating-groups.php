                        <h2>Operating Groups &amp; Mandates</h2>

                        <h3>UKTIA Executive Board</h3>
                        <h4>Operating Mandate</h4>
                        <p>Members of the Executive Board are formally registered with Companies House as UKTIA Directors.</p>
                        <p>The Directors are responsible for keeping proper accounting records, which disclose with reasonable accuracy at any time the financial position of the company and to enable them to ensure that the financial statements comply with the Companies Act of 1985.</p>
                        <p>To ensure that the income and property of UKTIA be applied solely towards the promotion of the objects of the UKTIA.</p>
                        <p>To determine annual membership contributions for all categories or to raise incremental monies for UKTIA activities as should be so determined and agreed by the board.</p>
                        <p>To set the strategy and direction for all UKTIA activities and to produce an annual business and budget plan that supports the objectives of the UKTIA.</p>
                        <p>To ensure that resource and managerial requirements needed to support UKTIA activities are attracted from across the membership population to ensure the widest possible engagement from members to deliver UKTIA objectives.</p>
                        <p>To ensure that the Officers leading the operating groups have an appropriate level of functional and managerial competence in their chosen field to manage and direct their group.</p>
                        <p>To ensure the assembly of an expert panel that will deal effectively with any crisis likely to impact on an individual member company or the industry in general, and to ensure that a Crisis Procedure exists, is in circulation, and is reviewed annually.</p>
                        <p>To ensure an active engagement with closely related industry bodies such as ETC and national associations, US and Canadian Tea Councils. Also, other competitive bodies such as the British Coffee Association, Coffee Science Information Centre and other tropical product groups and Government bodies / agencies such as DEFRA, DTI, FSA and DFID.</p>

                        <h3>Technical Committee</h3>
                        <h4>Technical Operating Group&nbsp;Mandate</h4>
                        <p>The Technical Operating Group supports the UKTIA by providing information, analysis and recommendations on all aspects of tea (Camellia sinensis and herbal infusions) throughout the supply chain to the finished product. It is made up of appointed representatives from member companies who support the UKTIA by providing technical, regulatory and supply chain expertise aimed at ensuring product safety, legality &amp; quality.</p>
                        <p>The key aims of the group are as follows:</p>
                        <ol>
                            <li>Provide advice for UKTIA stakeholders and consumers on any issues of a legal, technical nature</li>
                            <li>Provide a forum for the gathering and sharing of information that promotes &ldquo;Best Practice&rdquo; amongst UKTIA members</li>
                            <li>Monitor and review legislation, regulations, standards, codes of practice etc that relate to safety, legality &amp; quality</li>
                            <li>Anticipate, assess and lobby on any proposed changes or introduction of legislation, regulations, standards, codes of practice that may impact on the tea industry as a whole with the appropriate authorities</li>
                            <li>Maintain two way feedback with external bodies such as ETC and BSI/ISO and all relevant regulatory associations</li>
                            <li>Work closely with the ETC to ensure a harmonised approach is adopted</li>
                            <li>Support the harmonisation of Global standards</li>
                            <li>Provide technical input into the development of the UKTIA sustainability agenda, specifically relating to the consumer end of the supply chain</li>
                        </ol>

                        <p>In order to achieve the above, the Technical Operating Group meet on a regular basis, including sub-committee meetings via conference calls.</p>
                        <h4>Priorities</h4>
                        <ul>
                            <li>Contaminants - current and emerging
                            <ol>
                                <li>Pesticides : complience with EU MRL&#39;s, monitoring data, co-ordinated activity within Europe</li>
                                <li>Short - medium term : risk assessment based review of toxicological data and, where appropriate, lobbying regulatory associations to review these in line with most up to date information available</li>
                                <li>Long-term : Global action plan for controlling contaminants</li>
                            </ol>
                            </li>
                        </ul>

                        <ul>
                            <li>Nutrition &amp; Health Claims
                            <ol>
                                <li>Legislation : keep a watching brief and monitor activity within Europe and Globally</li>
                                <li>Where appropriate, lobbying regulatory and trade associations</li>
                                <li>Providing / approving technical information on behalf of the trade</li>
                            </ol>
                            </li>
                        </ul>


                        <h3>Marketing and Communications Operating Group</h3>
                        <h4>Operating Mandate</h4>
                        <p>The Marketing and Communications Committee is responsible for the generic marketing, communication and public relations activities of UKTIA which are designed to enhance and positively alter consumer attitudes to tea and consumption behaviour.</p>
                        <p>The Committee aims to carry out the following functions:</p>
                        <ol>
                            <li>Be responsible for the evaluation and analysis of market conditions.</li>
                            <li>Set the marketing and communications strategy for UKTIA</li>
                            <li>To propose an annual budget for marketing and communications.</li>
                            <li>Ensure the evaluation and performance of all programmes and support agencies are undertaken regularly.</li>
                            <li>Advise and guide the UKTIA Executive Chairman in the execution of activities.</li>
                            <li>Ensure that all activities and programmes undertaken are designed to enhance the performance of the category for the benefit of all members.</li>
                            <li>To ensure that all communications activities work within the legal and regulatory framework of the United Kingdom.</li>
                        </ol>

                        <h4>Priorities</h4>
                        <ol>
                            <li>Press Office &ndash; Proactive news campaign, editorial promotions, editorial radio campaign, radio competitions, journalist awareness</li>
                            <li>Tea4health.com &ndash; Website development</li>
                            <li>Tea4health in the surgery &ndash; consumer leaflet</li>
                            <li>Talking tea to HP&rsquo;s &ndash; HP Exhibitions, HP Media</li>
                            <li>Research &amp; Evaluation &ndash; message measurement, direct responses, quantitative research, consumption trends</li>
                        </ol>


