                        <h2>Crisis &amp; Response Procedures</h2>



                        <h3>
                            Introduction</h3>
                        <p>
                            In outlining this procedure I have specifically addressed a number of key requirements:</p>
                        <ol>
                            <li>
                                The need to establish accountability</li>
                            <li>
                                The identification of contact points within the industry for required expert opinion and advice.</li>
                            <li>
                                The establishment of a unified industry response to any crisis</li>
                            <li>
                                The communication of that unified response to the industry</li>
                            <li>
                                The identification of a spokesperson to the media</li>
                        </ol>
                        <h3>
                            Focal Point for Registering Industry Crisis Will Rest at The United Kingdom Tea &amp; Infusions Association Ltd (UKTIA)</h3>
                        <p>
                            When any members of the industry are alerted to a potential crisis they will in the first instance contact UKTIA.&nbsp; UKTIA will immediately contact the&nbsp;five major companies within the industry when it has sufficient information relating to the crisis.&nbsp; This advice system will ensure that all companies within the industry are aware of the emerging issues.</p>
                        <p>
                            The major companies within the Industry identified within this phase of the procedure are:&nbsp;</p>
                        <ol>
                            <li>
                                Unilever UK Foods</li>
                            <li>
                                Tata Global Beverages GB Ltd</li>
                            <li>
                                R Twining &amp; Co Ltd</li>
                            <li>
                                Finlay Beverages Ltd</li>
                            <li>
                                Taylors of Harrogate Ltd.</li>
                        </ol>
                        <p>
                            It is recommended that the contact points within the operating companies are the appointed Marketing Directors <em>(see appendix 1)</em>.&nbsp; It will be their responsibility to brief their own companies as considered appropriate.</p>
                        <p>
                            It is recognised that some crises may require immediate notification to the Managing Director or Public Relations Director in the absence of the Marketing Director.</p>
