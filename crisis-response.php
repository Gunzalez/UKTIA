                        <p>
                            The Expert Group will when appropriate, co-opt other members or industry experts when specific skills are required.</p>
                        <p>
                            In addition, UKTIA will liaise as necessary with international Tea Trade bodies and Producer Tea Boards and Associations that may have a direct interest in the issue.</p>
                        <h3>
                            Establishing an Industry Positioning Statement</h3>
                        <p>
                            It will be the UKTIA&#39;s executive chairman&rsquo;s responsibility, drawing on the knowledge within the Expert Group, to develop an Industry Positioning Statement, which will then be sent to nominated Directors within the identified major industry companies for their prompt approval.&nbsp; At the same time, and if necessary, the Statement will also be sent to the Executive Director of Ethical Tea Partnership.</p>
                        <p>
                            UKTIA will then issue the final agreed Industry Positioning Statement to all members of UKTIA and advise them that the industry has a considered position on the crisis and request companies to refer all media enquiries to UKTIA or the agreed spokesperson.</p>
                        <p>
                            In the event of the Executive Chairman of UKTIA being unavailable, Mr Nigel Holland, President, will assume this role and act as spokesperson.</p>
                        <h3>
                            International Communication</h3>
                        <p>
                            UKTIA will advise all other Tea Associations and Councils as appropriate.</p>
                        <h3>
                            Monitoring the Crisis</h3>
                        <p>
                            Through its Public Relations agency, UKTIA automatically runs a complete media monitoring service and will keep the key industry members up to date with coverage in the national press and will maintain a complete summary in-house.&nbsp;</p>
                        <h3>
                            Ongoing Management of the Crisis</h3>
                        <p>
                            UKTIA will advise the major companies of any foreseen change of press direction or perspective and in turn the operating companies will keep UKTIA updated on the questions raised by consumers or other groups.</p>
                        <h3>
                            Review Procedure</h3>
                        <p>
                            UKTIA will review the operation of the procedure and information annually and moderate where necessary.</p>
